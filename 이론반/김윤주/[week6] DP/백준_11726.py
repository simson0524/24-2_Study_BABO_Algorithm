"""
dp[1]	|	                                            1
dp[2]	||, =	                                        2
dp[3]	|||, |=, =|	                                    3
dp[4]	||||, ||=, |==|, =||, ==	                    5
dp[5]	|||||, |||=, ||=|, |=||, =|||, |==, =|=, ==|	8
dp[6]	||||||, ||||=, |||=|, ||=||, |=|||, =||||, ||==, |==|, ==|, |=|=, =|=|, =||=, ===   13
...
점화식 -> dp[x] = dp[x-1] + d[x-2]
"""

import sys
input = sys.stdin.readline

n = int(input())

dp = [0] * 1001
dp[1] = 1
dp[2] = 2

for i in range(3, n+1): 
    dp[i] = dp[i-1] + dp[i-2]
    
print(dp[n] % 10007)